"""Render deterministic synthetic benchmark TeX tables from summary JSON."""

from __future__ import annotations

import json
import sys
from pathlib import Path
from typing import Any

ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

SUMMARY_NAME = "synthetic_benchmark_summary.json"
SCORE_TABLE_NAME = "synthetic_benchmark_score_table.tex"
SUPPORT_TABLE_NAME = "synthetic_benchmark_support_table.tex"


def _output_paths(output_root: str | Path | None) -> dict[str, Path]:
    root = ROOT if output_root is None else Path(output_root)
    summary_path = root / "benchmarks" / "generated" / SUMMARY_NAME
    tex_generated_dir = root / "docs" / "tex" / "generated"
    tex_generated_dir.mkdir(parents=True, exist_ok=True)
    return {
        "summary_json": summary_path,
        "score_table_tex": tex_generated_dir / SCORE_TABLE_NAME,
        "support_table_tex": tex_generated_dir / SUPPORT_TABLE_NAME,
    }


def _tex_escape(text: str) -> str:
    return text.replace("_", r"\_")


def _format_float(value: float | None) -> str:
    if value is None:
        return "--"
    return f"{value:.3f}"


def _format_int(value: float | None) -> str:
    if value is None:
        return "--"
    return str(int(round(value)))


def _score_table(summary: dict[str, Any]) -> str:
    lines = [
        "% Auto-generated by benchmarks/render_tables.py from deterministic synthetic outputs.",
        "% Do not edit by hand.",
        r"\begin{table}[t]",
        r"\centering",
        r"\small",
        r"\begin{tabular}{llrrrrr}",
        r"\toprule",
        r"Scenario & Baseline & Test score & Accuracy & ARI & Fallbacks & Warnings \\",
        r"\midrule",
    ]
    for row in summary["aggregates"]["score_table_rows"]:
        lines.append(
            " & ".join(
                [
                    rf"\texttt{{{_tex_escape(row['scenario_id'])}}}",
                    rf"\texttt{{{_tex_escape(row['baseline'])}}}",
                    _format_float(row["test_score_mean"]),
                    _format_float(row["aligned_accuracy_mean"]),
                    _format_float(row["adjusted_rand_index_mean"]),
                    _format_int(row["fallback_count_mean"]),
                    _format_int(row["warning_count_mean"]),
                ]
            )
            + r" \\"
        )
    lines.extend(
        [
            r"\bottomrule",
            r"\end{tabular}",
            r"\caption{Small deterministic synthetic benchmark snapshot. Metrics are",
            r"averaged across fixed synthetic replicates and are not real-data evidence.}",
            r"\label{tab:synthetic_benchmark_score}",
            r"\end{table}",
        ]
    )
    return "\n".join(lines) + "\n"


def _support_table(summary: dict[str, Any]) -> str:
    lines = [
        "% Auto-generated by benchmarks/render_tables.py from deterministic synthetic outputs.",
        "% Do not edit by hand.",
        r"\begin{table}[t]",
        r"\centering",
        r"\small",
        r"\begin{tabular}{llrrrrr}",
        r"\toprule",
        r"Scenario & Baseline & Precision & Recall & F1 & True edges & Estimated edges \\",
        r"\midrule",
    ]
    for row in summary["aggregates"]["support_table_rows"]:
        lines.append(
            " & ".join(
                [
                    rf"\texttt{{{_tex_escape(row['scenario_id'])}}}",
                    rf"\texttt{{{_tex_escape(row['baseline'])}}}",
                    _format_float(row["support_precision_mean"]),
                    _format_float(row["support_recall_mean"]),
                    _format_float(row["support_f1_mean"]),
                    _format_int(row["true_edge_count_mean"]),
                    _format_int(row["estimated_edge_count_mean"]),
                ]
            )
            + r" \\"
        )
    lines.extend(
        [
            r"\bottomrule",
            r"\end{tabular}",
            r"\caption{Synthetic truth-based support diagnostics on deterministic benchmark",
            r"scenarios. The \texttt{oracle\_known\_labels} row uses sampled labels and is",
            r"an upper-reference, not a deployable estimator baseline.}",
            r"\label{tab:synthetic_benchmark_support}",
            r"\end{table}",
        ]
    )
    return "\n".join(lines) + "\n"


def main(output_root: str | Path | None = None) -> dict[str, str]:
    """Render TeX snippets from the benchmark summary JSON."""

    paths = _output_paths(output_root)
    summary = json.loads(paths["summary_json"].read_text(encoding="utf-8"))
    score_table = _score_table(summary)
    support_table = _support_table(summary)
    paths["score_table_tex"].write_text(score_table, encoding="utf-8")
    paths["support_table_tex"].write_text(support_table, encoding="utf-8")
    result = {
        "score_table": str(paths["score_table_tex"]),
        "support_table": str(paths["support_table_tex"]),
    }
    print(result)
    return result


if __name__ == "__main__":
    main()
